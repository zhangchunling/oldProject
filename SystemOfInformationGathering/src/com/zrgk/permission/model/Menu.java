package com.zrgk.permission.model;

import java.io.Serializable;

import com.zrgk.utils.Page;

public class Menu extends Page implements Serializable{
	
	private static final long serialVersionUID = 2110581088616242808L;
	private Integer mid;		//菜单主键
	private String menuNum;		//菜单编号
	private String menuName;	//菜单名称
	private String url;			//路径
	private Integer parentId;	//父id
	private String parentMenu;	//父菜单
	private Integer m_state; 	//状态
	public Integer getMid() {
		return mid;
	}
	public void setMid(Integer mid) {
		this.mid = mid;
	}
	public String getMenuNum() {
		return menuNum;
	}
	public void setMenuNum(String menuNum) {
		this.menuNum = menuNum;
	}
	public String getMenuName() {
		return menuName;
	}
	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public Integer getParentId() {
		return parentId;
	}
	public void setParentId(Integer parentId) {
		this.parentId = parentId;
	}
	public String getParentMenu() {
		return parentMenu;
	}
	public void setParentMenu(String parentMenu) {
		this.parentMenu = parentMenu;
	}
	public Integer getM_state() {
		return m_state;
	}
	public void setM_state(Integer m_state) {
		this.m_state = m_state;
	}
	
	
	
}
